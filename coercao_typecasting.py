# conversão de tipos, coerção
# type convertion, typecasting, coersion
# é o ato de converter um tipo em outro
# tipos imutavéis e primitivos:
# str, float, int, bool
print(1 + 1)
print('a' + 'b') #polimorfismo, concatenou (juntou)
print(type('1'))
print(int('1'), type(int('1')))
print(type(float('1') + 1))
print(bool('1'))
print(bool(''))
#typecasting --> conversão!
print(str(11) + 'b') 
